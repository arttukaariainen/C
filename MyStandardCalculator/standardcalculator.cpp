#include "standardcalculator.h"

StandardCalculator::StandardCalculator(){}
StandardCalculator::~StandardCalculator(){}

double StandardCalculator::plus(double a, double b)
{
    return a+b;
}
double StandardCalculator::minus(double a, double b)
{
    return a-b;
}
double StandardCalculator::mult(double a, double b)
{
    return a*b;
}
double StandardCalculator::div(double a, double b)
{
    return a/b;
}
